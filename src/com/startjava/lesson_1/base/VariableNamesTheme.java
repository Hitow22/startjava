package com.startjava.lesson_1.base;

public class VariableNamesTheme {
    public static void main(String[] args) {
        System.out.println("1. Разные переменные");

        // Цифра
        int number = 3;
        System.out.println("Цифра = " + number);

        // Сумма чисел
        int numbersSum = 7;
        System.out.println("Сумма чисел = " + numbersSum);

        // Произведение цифр
        int numbersMult = 8;
        System.out.println("Произведение цифр = " + numbersMult);

        // Максимальное число
        int maxNumber = 5;
        System.out.println("Максимальное число = " + maxNumber);

        // Количество десятков
        int tensCount = 2;
        System.out.println("Количество десятков = " + tensCount);

        // Вес собаки
        double dogWeight = 16.5;
        System.out.println("Вес собаки = " + dogWeight);

        // Исходное число
        int originalNumber = 777;
        System.out.println("Исходное число = " + originalNumber);

        // Процент по вкладу
        double depositPercent = 6.9;
        System.out.println("Процент по вкладу = " + depositPercent);

        // Переменная хранит символ &
        char symbol = '&';
        System.out.println("Переменная хранит символ & = " + symbol);

        // Код ошибки
        int errorCode = 404;
        System.out.println("Код ошибки = " + errorCode);

        // Тип сообщения
        String messageType = "Срочное";
        System.out.println("Тип сообщения = " + messageType);

        // Число нулей
        int nullCount = 6;
        System.out.println("Число нулей = " + nullCount);

        // Уникальное число
        int uniqueNumber = 666;
        System.out.println("Уникальное число = " + uniqueNumber);

        // Случайное число
        int randomNumber = 457;
        System.out.println("Случайное число = " + randomNumber);

        // Математическое выражение
        int mathExpression = 88 + 88;
        System.out.println("Математическое выражение = " + mathExpression);

        // Счет в игре
        double score = 10.5;
        System.out.println("Счет в игре = " + score);

        // Максимальная длина
        double maxLength = 9999.99;
        System.out.println("Максимальная длина = " + maxLength);

        // Пункт меню
        int menuItem = 5;
        System.out.println("Пункт меню = " + menuItem);

        // Стоимость кофе
        double coffeeCost = 6.4;
        System.out.println("Стоимость кофе = " + coffeeCost);

        // Дата начала
        double startDate = 21.01;
        System.out.println("Дата начала = " + startDate);

        // Окончание диапазона
        int endOfRange = 9;
        System.out.println("Окончание диапазона = " + endOfRange);

        // Полное имя работника месяца
        String fullName = "Alexey Dmitriev";
        System.out.println("Полное имя работника месяца = " + fullName);

        // Заголовок электронной книги
        String heading = "End";
        System.out.println("Заголовок электронной книги = " + heading);

        // Размер
        int size = 46;
        System.out.println("Размер = " + size);

        // Вместимость
        int capacity = 1000;
        System.out.println("Вместимость = " + capacity);

        // Счетчик
        int counter = 5;
        System.out.println("Счетчик = " + counter);

        // Путь до файла
        String path = "/path/to/file.txt";
        System.out.println("Путь до файла = " + path);

        // Количество чисел в каждой строке
        int numbersInEachRow = 21;
        System.out.println("Количество чисел в каждой строке = " + numbersInEachRow);

        System.out.println("\n2. Boolean-переменные");

        // Сотни равны?
        boolean isEqualsHundreds = true;
        System.out.println("Сотни равны? = " + isEqualsHundreds);

        // Компьютер включен?
        boolean isComputerOn = true;
        System.out.println("Компьютер включен? = " + isComputerOn);

        // Есть равные цифры?
        boolean hasEqualDigits = false;
        System.out.println("Есть равные цифры? = " + hasEqualDigits);

        // Создано?
        boolean isCreated = true;
        System.out.println("Создано? = " + isCreated);

        // Пустое?
        boolean isEmpty = false;
        System.out.println("Пустое? = " + isEmpty);

        // Активное?
        boolean isActive = true;
        System.out.println("Активное? = " + isActive);

        // Новое?
        boolean isNew = true;
        System.out.println("Новое? = " + isNew);

        // Электронная почта действительная?
        boolean isValidEmail = true;
        System.out.println("Электронная почта действительная? = " + isValidEmail);

        // Имеются уникальные строки?
        boolean hasUniqueStrings = false;
        System.out.println("Имеются уникальные строки? = " + hasUniqueStrings);

        System.out.println("\n3. Аббревиатуры");

        // Старый universally unique identifier
        String oldUuid = "UUID";
        System.out.println("Старый universally unique identifier = " + oldUuid);

        // Производитель оперативной памяти
        String ramManufacturer = "Kingston";
        System.out.println("Производитель оперативной памяти = " + ramManufacturer);

        // Емкость жесткого диска
        double hddCapacity = 333.5;
        System.out.println("Емкость жесткого диска = " + hddCapacity);

        // Протокол передачи гипертекста
        String http = "HTTP";
        System.out.println("Протокол передачи гипертекста = " + http);

        // Сокращенный uniform resource locator
        String shortUrl = "URL";
        System.out.println("Сокращенный uniform resource locator = " + shortUrl);

        // Новый идентификатор клиента
        int newCid = 1;
        System.out.println("Новый идентификатор клиента = " + newCid);

        // American standard code for information interchange
        String ascii = "ASCII";
        System.out.println("American standard code for information interchange = " + ascii);
    }
}
